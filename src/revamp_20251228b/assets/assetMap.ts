const toThemeVariant = (theme: ThemeMode): ThemeVariant =>
  theme === "yang" ? "light" : "dark";

export const getIconSrc = (theme: ThemeMode, key: IconKey): string =>
  iconMap[toThemeVariant(theme)][key];

export const getBrandSrc = (theme: ThemeMode, key: BrandKey): string =>
  brandMap[toThemeVariant(theme)][key];

export const getBackgroundSrc = (theme: ThemeMode, key: BackgroundKey): string =>
  backgroundMap[toThemeVariant(theme)][key];

// Zodiac: rooster includes rooster head; no mixed zodiac icons.
export const getZodiacSrc = (theme: ThemeMode, zodiac: ZodiacKey): string =>
  zodiacMap[toThemeVariant(theme)][zodiac];

// Five elements: regular = normal brush, mono = textured BW, texture = colored.
export const getElementSrc = (
  theme: ThemeMode,
  element: ElementKey,
  variant: ElementVariant,
): string => elementMap[toThemeVariant(theme)][element][variant];

// Directions: no mixed-direction images; compose in UI. No arrows baked in.
export const getDirectionSrc = (
  theme: ThemeMode,
  direction: DirectionKey,
): string => directionMap[toThemeVariant(theme)][direction];

// Arrows: rotate/position via CSS; reused as back button assets.
export const getArrowSrc = (theme: ThemeMode, arrow: ArrowKey): string =>
  arrowMap[toThemeVariant(theme)][arrow];

// Notification: same artwork; filename encodes state.
export const getNotificationSrc = (
  theme: ThemeMode,
  state: NotificationKey,
): string => notificationMap[toThemeVariant(theme)][state];

export const getDestinySrc = (theme: ThemeMode, key: DestinyKey): string =>
  destinyMap[toThemeVariant(theme)][key];

export const getUiSymbolSrc = (
  theme: ThemeMode,
  symbol: UiSymbolKey,
): string => uiSymbolMap[toThemeVariant(theme)][symbol];

